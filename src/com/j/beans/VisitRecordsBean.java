package com.j.beans;

import java.util.Date;

public class VisitRecordsBean {

	private Date attenddate;
	private String reportsLink;
	private String reportsNote;
	public Date getAttenddate() {
		return attenddate;
	}
	public void setAttenddate(Date attenddate) {
		this.attenddate = attenddate;
	}
	public String getReportsLink() {
		return reportsLink;
	}
	public void setReportsLink(String reportsLink) {
		this.reportsLink = reportsLink;
	}
	public String getReportsNote() {
		return reportsNote;
	}
	public void setReportsNote(String reportsNote) {
		this.reportsNote = reportsNote;
	}
	
	
}
